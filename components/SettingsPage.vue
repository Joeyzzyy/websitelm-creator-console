                                <div 
                                  class="deployment-option"
                                  :class="{ active: deploymentForm.method === 'subfolder' }"
                                  @click="deploymentForm.method = 'subfolder'"
                                >
                                  <div class="option-header">
                                    <div class="radio-wrapper">
                                      <a-radio :checked="deploymentForm.method === 'subfolder'" />
                                    </div>
                                    <h4>Subfolder</h4>
                                  </div>
                                  <p class="option-description">
                                    Deploy your content in a subfolder (e.g., yourdomain.com/blog)
                                  </p>
                                </div>
                              </div>
                            </a-form-item>

                            <div class="input-section" v-if="deploymentForm.method === 'subfolder'">
                              <a-spin :spinning="subfolderLoading">
                                <div class="current-domain">
                                  <h3>Current Site</h3>
                                  <div class="domain-verification">
                                    <p class="website-url">{{ productInfo?.projectWebsite }}</p>
                                    <a-button 
                                      v-if="!subfolderDomainConfig"
                                      type="primary"
                                      @click="startVerifyingSubfolder"
                                      :loading="verifyingSubfolder"
                                    >
                                      Start Verifying
                                    </a-button>
                                  </div>
                                </div>

                                <div class="verification-status" v-if="subfolderDomainConfig">
                                  <div class="domains-header">
                                    <h3>Domain Verification</h3>
                                    <div class="domain-actions">
                                      <a-button 
                                        type="link"
                                        class="refresh-btn"
                                        :loading="refreshingSubfolder"
                                        @click="handleSubfolderRefresh"
                                      >
                                        <reload-outlined />
                                      </a-button>
                                    </div>
                                  </div>
                                  
                                  <template v-if="subfolderDomainConfig.misconfigured">
                                    <div class="dns-config-alert">
                                      <a-alert
                                        type="warning"
                                        show-icon
                                        message="DNS Configuration Required"
                                        description="Please add the following DNS record to your domain provider:"
                                      />
                                      <div class="dns-table-wrapper">
                                        <a-table
                                          :dataSource="[{
                                            type: 'A',
                                            name: '@',
                                            value: '76.76.21.21'
                                          }]"
                                          :columns="dnsColumnsWithActions"
                                          :pagination="false"
                                          size="small"
                                          class="dns-table"
                                        />
                                      </div>
                                    </div>
                                  </template>
                                  <template v-else>
                                    <div class="verification-header">
                                      <a-alert
                                        type="success"
                                        show-icon
                                        message="Domain Verified"
                                        description="Your domain has been successfully verified"
                                        class="success-alert"
                                      >
                                        <template #message>
                                          <div class="alert-content">
                                            <span>Domain Verified</span>
                                          </div>
                                        </template>
                                        <template #action>
                                          <a-button 
                                            type="link" 
                                            danger
                                            @click="showDeleteSubfolderDomainConfirm"
                                            class="delete-btn"
                                          >
                                            <delete-outlined />
                                          </a-button>
                                        </template>
                                      </a-alert>
                                    </div>
                                  </template>
                                </div>

                                <template v-if="subfolderDomainConfig">
                                  <div class="verification-status">
                                    <!-- ... 验证状态显示部分保持不变 ... -->
                                  </div>

                                  <!-- 只在域名验证成功(非misconfigured)时显示子文件夹管理部分 -->
                                  <template v-if="!subfolderDomainConfig.misconfigured">
                                    <div class="section-header">
                                      <h3>Subfolder Management</h3>
                                      <a-button type="primary" @click="showSubfolderModal = true">
                                        Add Subfolder
                                      </a-button>
                                    </div>

                                    <div class="subfolder-list">
                                      <a-table 
                                        :dataSource="subfolders" 
                                        :columns="subfolderColumns" 
                                        :pagination="false"
                                      >
                                        <template #bodyCell="{ column, text, record, index }">
                                          <template v-if="column.key === 'action'">
                                            <a-button type="link" danger @click="confirmDeleteSubfolder(index)">
                                              Delete
                                            </a-button>
                                          </template>
                                          <template v-if="column.key === 'preview'">
                                            <span>{{ productInfo?.projectWebsite }}/{{ text }}</span>
                                          </template>
                                        </template>
                                      </a-table>
                                    </div>
                                  </template>
                                </template>
                              </a-spin>
                            </div>
                            <div class="input-section" v-if="deploymentForm.method === 'subdomain'">
                              <a-form-item 
                                label="Enter your subdomain"
                                required
                              >
                                <div class="url-preview">
                                  <a-input
                                    v-model:value="deploymentForm.prefix"
                                    placeholder="blog"
                                    :disabled="saving"
                                    class="custom-input"
                                  />
                                  <span class="url-separator">.</span>
                                  <span class="base-domain">{{ productInfo?.projectWebsite }}</span>
                                </div>
                              </a-form-item>
                              <a-form-item>
                                <a-button 
                                  type="primary"
                                  :loading="saving"
                                  @click="saveDeploymentSettings"
                                  class="save-btn"
                                >
                                  Add
                                </a-button>
                              </a-form-item>

                              <a-spin :spinning="subdomainLoading">
                                <div class="configured-domains" v-if="currentDomainConfigs?.length > 0">
                                  <div class="domains-header">
                                    <h3>Added Subdomains</h3>
                                    <a-button 
                                      type="link"
                                      class="refresh-btn"
                                      :loading="refreshing"
                                      @click="handleRefresh"
                                    >
                                      <reload-outlined />
                                    </a-button>
                                  </div>
                                  <div class="domain-list">
                                    <div v-for="domain in currentDomainConfigs" :key="domain.name" class="domain-item">
                                      <div class="domain-info">
                                        <span class="domain-name">{{ domain.name }}</span>
                                        <a-tag :color="getDomainStatusColor(domain)">
                                          {{ getDomainStatusText(domain) }}
                                        </a-tag>
                                      </div>
                                      <template v-if="domain.configDetails?.misconfigured">
                                        <div class="dns-config-alert">
                                          <a-alert
                                            type="warning"
                                            show-icon
                                            message="DNS Configuration Required"
                                            description="Please add the following DNS record to your domain provider:"
                                          />
                                          <div class="dns-table-wrapper">
                                            <a-table
                                              :dataSource="[{
                                                type: 'CNAME',
                                                name: domain.name.split('.')[0],
                                                value: 'cname.vercel-dns.com.'
                                              }]"
                                              :columns="dnsColumns"
                                              :pagination="false"
                                              size="small"
                                              class="dns-table"
                                            >
                                            </a-table>
                                          </div>
                                        </div>
                                      </template>
                                      <div class="domain-actions">
                                        <span class="domain-date">Added on {{ new Date(domain.createdAt).toLocaleDateString() }}</span>
                                        <a-button 
                                          type="link" 
                                          danger
                                          @click="showDeleteConfirm(domain.name)"
                                        >
                                          <delete-outlined />
                                        </a-button>
                                      </div>
                                    </div>
                                  </div>
                                </div>
                              </a-spin>
                            </div>
                          </a-form>
                        </div>

                        <a-modal
                          v-model:visible="showSubfolderModal"
                          title="Add Subfolder"
                          @ok="handleAddSubfolder"
                          :confirmLoading="savingSubfolders"
                          :okButtonProps="{ disabled: !newSubfolder }"
                        >
                          <div class="subfolder-input">
                            <span class="base-url">{{ productInfo?.projectWebsite }}/</span>
                            <a-input v-model:value="newSubfolder" placeholder="Enter subfolder name" />
                          </div>
                        </a-modal>
                      </div>
                  </div>
                </div>
              </a-spin>
            </template>

            <template v-if="activeTab === 'login-email'">
              <div class="account-settings">
                <div class="current-email-section">
                  <h3>Current Login Email</h3>
                  <div class="email-display">
                    <a-input
                      v-model:value="currentEmail"
                      disabled
                      class="current-email-input"
                    />
                    <a-button 
                      type="primary"
                      @click="showChangeEmailForm"
                      v-if="!showEmailForm"
                      class="change-email-btn"
                    >
                      Change Login Email
                    </a-button>
                  </div>
                </div>

                <div class="email-change-section" v-if="showEmailForm">
                  <div class="section-header">
                    <h3>Change Login Email</h3>
                    <a-button 
                      @click="cancelEmailChange"
                      class="cancel-btn"
                    >
                      <close-outlined />
                    </a-button>
                  </div>
                  <div class="email-change-form">
                    <a-form
                      :model="emailForm"
                      layout="vertical"
                    >
                      <a-form-item 
                        label="New Email Address"
                        required
                      >
                        <div class="email-input-group">
                          <a-input
                            v-model:value="emailForm.newEmail"
                            placeholder="Enter new email address"
                            class="custom-input"
                          />
                          <a-button 
                            type="primary"
                            :loading="sendingCode"
                            :disabled="!isValidEmail || cooldown > 0"
                            @click="sendVerificationCode"
                            class="send-code-btn"
                          >
                          {{ cooldown > 0 ? `${cooldown}s` : 'Send Code' }}
                          </a-button>
                        </div>
                      </a-form-item>

                      <a-form-item 
                        label="Verification Code"
                        required
                      >
                        <a-input
                          v-model:value="emailForm.code"
                          placeholder="Enter verification code"
                          class="custom-input"
                        />
                      </a-form-item>

                      <a-form-item>
                        <a-button 
                          type="primary"
                          :loading="changing"
                          @click="confirmEmailChange"
                          :disabled="!canSubmit"
                          class="save-btn"
                        >
                          Change Now
                        </a-button>
                      </a-form-item>
                    </a-form>
                  </div>
                </div>
              </div>
            </template>
          </div>
        </div>
      </div>
    </template>
  </page-layout>
</template>

<script>
import { ref, onMounted, watch, computed, onBeforeUnmount } from 'vue';
import { message, Modal } from 'ant-design-vue';
import PageLayout from './layout/PageLayout.vue'
import { useRouter } from 'vue-router';
import { SettingOutlined, DeleteOutlined, ReloadOutlined, CloseOutlined } from '@ant-design/icons-vue'
import apiClient from '../api/api';
import { VERCEL_CONFIG } from '../config/vercelConfig';
import NoSiteConfigured from './common/NoSiteConfigured.vue'
import SmartBanner from './common/SmartBanner.vue'


export default {
  name: 'SettingsPage',
  components: {
    PageLayout,
    SettingOutlined,
    DeleteOutlined,
    ReloadOutlined,
    CloseOutlined,
    NoSiteConfigured,
    SmartBanner,
  },
  setup() {
    const PROJECT_ID = VERCEL_CONFIG.PROJECT_ID;
    const cooldown = ref(0);
    const cooldownTimer = ref(null);
    const router = useRouter();
    const goToDashboard = () => {
      router.push('/dashboard');
    };
    const activeTab = ref('domains');
    const domainLoading = ref(false);
    const subdomainLoading = ref(false);
    const domainModalVisible = ref(false);
    const savingDomain = ref(false);
    const websitePrefix = ref('https://');
    const domainForm = ref({
      domain: ''
    });
    const deploymentForm = ref({
      method: 'subdomain',
      prefix: '',
      suffix: '',
    });
    const saving = ref(false);
    const productInfo = ref(null);
    const vercelDomainInfo = ref(null);
    const currentDomainConfigs = ref([]);
    const dnsColumns = [
      {
        title: 'Type',
        dataIndex: 'type',
        key: 'type',
      },
      {
        title: 'Name',
        dataIndex: 'name',
        key: 'name',
      },
      {
        title: 'Value',
        dataIndex: 'value',
        key: 'value',
      }
    ];
    const refreshing = ref(false);
    const handleRefresh = async () => {
      refreshing.value = true;
      try {
        await loadVercelDomainInfo();
        message.success('Refresh successfully');
      } catch (error) {
        message.error('Refresh failed');
      } finally {
        refreshing.value = false;
      }
    };

    const currentEmail = ref(localStorage.getItem('currentCustomerEmail') || '');
    const showEmailForm = ref(false);
    const emailForm = ref({
      newEmail: '',
      code: ''
    });
    const sendingCode = ref(false);
    const changing = ref(false);

    const isValidEmail = computed(() => {
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
      return emailRegex.test(emailForm.value.newEmail);
    });

    const canSubmit = computed(() => {
      return isValidEmail.value && emailForm.value.code.length > 0;
    });

    const showChangeEmailForm = () => {
      showEmailForm.value = true;
    };

    const sendVerificationCode = async () => {
      if (emailForm.value.newEmail === currentEmail.value) {
        message.error('Please enter a different email address from your current one');
        return;
      }
      sendingCode.value = true;
      try {
        const response = await apiClient.sendEmailCode(emailForm.value.newEmail, 'change_email');
        // 开始倒计时
        console.log('开始倒计时');
        cooldown.value = 60;
        console.log('当前cooldown值:', cooldown.value);
        cooldownTimer.value = setInterval(() => {
          cooldown.value--;
          console.log('倒计时:', cooldown.value);
          if (cooldown.value <= 0) {
            clearInterval(cooldownTimer.value);
          }
        }, 1000);
        if (response?.code === 200) {
          message.success('Code sent successfully');
        } else {
          message.error('Error sending code');
        }
      } catch (error) {
        console.error('Error sending code:', error);
        message.error('Error sending code, please try again later');
      } finally {
        sendingCode.value = false;
      }
    };

    const confirmEmailChange = () => {
      Modal.confirm({
        title: 'Confirm Email Change',
        content: emailForm.value.newEmail.includes('gmail.com') 
          ? 'After changing, the original email account will no longer be able to log in this account. Do you want to continue?'
          : 'Are you sure you want to change your login email?',
        okText: 'Confirm',
        cancelText: 'Cancel',
        async onOk() {
          changing.value = true;
          try {
            const response = await apiClient.changeEmail({
              newEmail: emailForm.value.newEmail,
              code: emailForm.value.code
            });

            if (response?.code === 200) {
              // 更新地存储的邮箱
              currentEmail.value = emailForm.value.newEmail;
              localStorage.setItem('currentCustomerEmail', emailForm.value.newEmail);
              
              // 重置表单
              showEmailForm.value = false;
              emailForm.value = { 
                newEmail: '', 
                code: '' 
              };
              
              message.success('Successfully changed email');

              // 显示登出提示并倒计时
              let secondsLeft = 3;
              const modal = Modal.success({
                title: 'Success',
                content: `You will be logged out in ${secondsLeft} seconds, please use the new email to log in`,
                okText: 'OK',
                keyboard: false,
                maskClosable: false,
              });

              const timer = setInterval(() => {
                secondsLeft -= 1;
                modal.update({
                  content: `You will be logged out in ${secondsLeft} seconds, please use the new email to log in`,
                });

                if (secondsLeft <= 0) {
                  clearInterval(timer);
                  modal.destroy();
                  // 执行登出操作
                  localStorage.removeItem('authToken');
                  localStorage.removeItem('intelickIsLoggedIn');
                  localStorage.removeItem('currentCustomer');
                  localStorage.removeItem('currentCustomerId'); 
                  router.push('/login');
                  message.success('Logged out, please use the new email to log in');
                }
              }, 1000);
            } else {
              if(response?.code === 400 && response?.message === 'Email already exists') {
                message.error('The new email address has already been registered, please try another one');
              } else if (response?.code === 400 && response?.message === 'Invalid verification code') {
                message.error('The verification code is incorrect, please try again');
              } else {
                message.error('Failed to change email, please try again later');
              }
            }
          } catch (error) {
            console.error('Failed to change email:', error);
            message.error(error.response?.data?.message || 'Failed to change email, please try again later');
          } finally {
            changing.value = false;
          }
        }
      });
    };

    const cancelEmailChange = () => {
      showEmailForm.value = false;
      emailForm.value = {
        newEmail: '',
        code: ''
      };
    };

    const showDeleteConfirm = (domainName) => {
      Modal.confirm({
        title: 'Confirm Delete',
        content: `Are you sure you want to delete subdomain ${domainName}?`,
        okText: 'Delete',
        okType: 'danger',
        cancelText: 'Cancel',
        async onOk() {
          try {
            await apiClient.deleteVercelDomain(PROJECT_ID, domainName);
            message.success('Domain deleted successfully');
            await loadVercelDomainInfo();
          } catch (error) {
            console.error('Failed to delete domain:', error);
            message.error('Failed to delete domain');
          }
        },
      });
    };

    // 添加域名配置状态
    const domainConfigured = ref(false);
    const loading = ref(false);

    // 加载产品信息
    const loadProductInfo = async () => {
      loading.value = true;
      try {
        const customerId = localStorage.getItem('currentCustomerId');
        const response = await apiClient.getProductsByCustomerId(customerId);
        
        if (response?.code === 200 && response.data) {
          productInfo.value = response.data;
          // 修改为与 AssetsPage 一致的判断逻辑
          domainConfigured.value = response.data?.domainStatus || false;
          // 重置表单状态
          deploymentForm.value = {
            method: 'subdomain',
            prefix: '',
            suffix: '',
          };
          // 加载 Vercel 域名信息
          if (domainConfigured.value) {
            await loadVercelDomainInfo();
          }
        } else {
          productInfo.value = null;
          domainConfigured.value = false;
        }
      } catch (error) {
        console.error('Failed to load product information:', error);
        productInfo.value = null;
        domainConfigured.value = false;
      } finally {
        loading.value = false;
      }
    };

    // 处理 Vercel 域名信息
    const processVercelDomainInfo = async (response) => {
      if (!response?.domains || !productInfo.value?.projectWebsite) {
        currentDomainConfigs.value = [];
        return;
      }

      const currentDomain = productInfo.value.projectWebsite;
      
      // 过滤匹配的域名，排除主域名
      const matchingDomains = response.domains.filter(domain => {
        return domain.apexName === currentDomain && domain.name !== currentDomain;
      });

      // 为每个域名获取详细配置
      const domainsWithConfig = await Promise.all(
        matchingDomains.map(async (domain) => {
          try {
            const customerId = localStorage.getItem('currentCustomerId');
            let projectId = 'nextjs';
            
            if (customerId === '67525da4ba5fcadf228e56c1') {
              projectId = 'prj_7SXIhcIx5SOYKKVzhhRvnmUdoN7g';
            }

            const config = await apiClient.getVercelDomainConfig(domain.name);
            return {
              ...domain,
              verified: config.verified || false,
              configDetails: config
            };
          } catch (error) {
            console.error(`Failed to get config for domain ${domain.name}:`, error);
            return {
              ...domain,
              verified: false,
              configDetails: null
            };
          }
        })
      );

      currentDomainConfigs.value = domainsWithConfig;
      console.log('域名配置更新：', currentDomainConfigs.value);
    };

    // 添加新的方法来获取 Vercel 域名信息
    const loadVercelDomainInfo = async () => {
      try {
        subdomainLoading.value = true;
        const projectId = PROJECT_ID;

        const response = await apiClient.getVercelDomainInfo(projectId);
        vercelDomainInfo.value = response;
        await processVercelDomainInfo(response);
      } catch (error) {
        console.error('Failed to load Vercel domain info:', error);
        message.error('Failed to load domain information');
      } finally {
        subdomainLoading.value = false;
      }
    };

    // 监听 activeTab 变化
    watch(activeTab, (newValue) => {
      if (newValue === 'domains') {
        loadProductInfo();
        // 当切换到domains标签时，如果选择了subfolder方法，则加载子文件夹数据
        if (deploymentForm.value.method === 'subfolder') {
          loadSubfolders();
        }
      } else if (newValue === 'login-email') {
        showEmailForm.value = false;
        emailForm.value = {
          newEmail: '',
          code: ''
        };
      }
    });

    // 监听部署方法的变化
    watch(() => deploymentForm.value.method, (newMethod) => {
      if (newMethod === 'subfolder') {
        loadSubfolders();
      }
    });

    // 修改loadSubfolders方法
    const loadSubfolders = async () => {
      try {
        subfolderLoading.value = true;
        const response = await apiClient.getSubfolders();
        if (response?.code === 200 && response?.data) {
          // 直接将字符串数组转换为所需的对象格式
          subfolders.value = response.data.map(subfolder => ({
            text: subfolder,
            preview: subfolder  // 这里的 preview 会在表格中显示完整路径
          }));
        } else {
          subfolders.value = [];
        }
      } catch (error) {
        console.error('Failed to load subfolders:', error);
        message.error('Failed to load subfolder list');
        subfolders.value = [];
      } finally {
        subfolderLoading.value = false;
      }
    };

    // 在组件挂载时加载产品信息
    onMounted(() => {
      loadProductInfo();
    });

    onBeforeUnmount(() => {
      if (cooldownTimer.value) {
        clearInterval(cooldownTimer.value);
      }
    });

    // 部署设置相关方法
    const saveDeploymentSettings = async () => {
      if (!deploymentForm.value.method) {
        message.error('Please select a deployment method');
        return;
      }

      if (deploymentForm.value.method === 'subdomain' && !deploymentForm.value.prefix) {
        message.error('Please enter a subdomain prefix');
        return;
      }

      if (deploymentForm.value.method === 'subdomain') {
        const currentSubdomains = currentDomainConfigs.value.length;
        if (currentSubdomains >= 2) {
          message.error('You can only add up to 2 subdomains');
          return;
        }
